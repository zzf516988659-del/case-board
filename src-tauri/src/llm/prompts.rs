//! 字段抽取的 prompt 工程。
//!
//! 集中在这里方便迭代——改 prompt 后跑 `cargo test` + 手工 e2e 测一次就行。

/// 2026-05-23 晚十 作者建议:根据文件名/分类知道这是什么材料,告诉 LLM 让它更针对性。
///
/// 例:`category="判决书"` 时强调"找判决金额 + 生效日";`category="保全申请"` 时找"保全标的 + 期限"。
///
/// 2026-05-23 晚十五 作者反馈:
///   1) 反诉文档会污染 plaintiffs/defendants —— 必须以原诉视角填写,反诉的原被告要互换
///   2) party_contacts 只关心我方,加 is_our_side 标记
///   3) 收费记录优先抽律师费(从代理合同),其次保函费/案件受理费/保全费
///   4) key_dates 只保留办案过程节点(接案/申请立案/正式立案/保全/开庭/调解/判决/上诉/二审开庭/开发票)
///      LPR、违约金计算、数字大写这些都不要
pub fn case_fields_extraction_with_hint(
    text: &str,
    filename: Option<&str>,
    category: Option<&str>,
) -> String {
    // 根据 category 给出有针对性的提示
    let hint = match category {
        Some("起诉状") | Some("民事起诉状") => {
            "这是【起诉状】,我方=原告。重点找:案由、原告(我方)、被告(对方)、第三人、诉讼请求金额、起诉日期。\
             party_contacts 里我方(原告及其代理人)is_our_side=true。\
             key_dates 加一条 {event_type:\"申请立案\", date:起诉日期}。"
        }
        Some("判决书") | Some("民事判决") => {
            "这是【判决书】,重点找:案号、法院、当事人、判决金额、案由、审理法官、判决日期。\
             ⚠️ 如果是反诉判决,plaintiffs/defendants 仍按【原诉视角】填(原诉原告→plaintiffs)。\
             多项金额时取本金或'判决主文'里的主要金额。\
             key_dates 加 {event_type:\"判决\", date:判决日期}。"
        }
        Some("裁定书") | Some("民事裁定") => {
            "这是【裁定书】,重点找:案号、法院、当事人、裁定内容相关金额、日期。"
        }
        Some("调解书") => {
            "这是【调解书】,重点找:案号、法院、当事人、调解协议金额(本金+违约金合计)、还款约定、审理法官。\
             调解金额可能跟原诉讼金额不同,以调解协议为准。\
             key_dates 加 {event_type:\"调解\", date:调解日期}。"
        }
        Some("受理通知") | Some("应诉通知") => {
            "这是【受理/应诉通知】,重点找:案号、法院、立案日期(=正式立案)、承办法官、当事人。\
             key_dates 加 {event_type:\"正式立案\", date:法院立案日期}。"
        }
        Some("财产保全") | Some("财产保全申请") => {
            "这是【财产保全申请/裁定】,重点找:申请保全的标的(账户/股权/房产/车辆)、保全金额、申请人(我方)、被申请人(对方)。\
             preservations 字段填详细;另外 key_dates 加 {event_type:\"保全\", date:保全申请/裁定日期}。\
             ⚠️ fees 里若有保险公司/担保公司开具保函的费用,item 填 \"保函费\"。"
        }
        Some("传票") | Some("开庭通知") => {
            "这是【传票/开庭通知】,重点找:案号、开庭日期、法庭、当事人。\
             key_dates 加 {event_type:\"开庭\", date:开庭日期, note:\"开庭时间 + 法庭/地点 + 庭次备注\"}(每次开庭一条记录;\
             若文书写了应到时间/庭审时间/第几法庭/开庭地点,优先塞进 note,例:\"13:30 第6法庭\"、\"14:30-16:00 第6法庭 钱潮路369号801室\")。"
        }
        Some("上诉状") => {
            "这是【上诉状】,我方=上诉人(plaintiffs)。重点找:案号、上诉人、被上诉人、上诉理由对应的金额。\
             key_dates 加 {event_type:\"上诉\", date:上诉提交日期}。"
        }
        Some("答辩状") => {
            "这是【答辩状】,我方=答辩人(若文书表头写'答辩人:X',X 进 plaintiffs/defendants 看原诉视角——\
             答辩通常是我方被告方答辩,把答辩人放到 defendants 不变,但 party_contacts 里答辩人方 is_our_side=true)。"
        }
        Some("限制消费令") | Some("失信被执行人") | Some("终本裁定") => {
            "这是【执行类文书】,重点找:案号、被执行人、申请执行人、执行法院。"
        }
        Some("委托合同") | Some("代理合同") | Some("律师代理合同") | Some("聘请律师合同") => {
            "这是【委托/代理合同】,委托方=我方。重点找:\
             ① party_contacts:委托方个人/公司及联系人 is_our_side=true;\
             ② fees:必抽一条 {item:\"律师代理费\", amount:合同约定的律师费, charged_at:支付时间, note:支付方式/分期约定};\
             ③ key_dates 加 {event_type:\"接案\", date:合同签订日期}。\
             律师代理费金额是最核心信息,合同里一般写'代理费 / 律师费 / 服务费 人民币 X 元'。"
        }
        _ => "",
    };

    let context_line = match (filename, category) {
        (Some(fn_), Some(cat)) if !cat.is_empty() => {
            format!("\n文件名: {}\n文档类型: {}\n{}\n", fn_, cat, hint)
        }
        (Some(fn_), _) => format!("\n文件名: {}\n", fn_),
        _ => String::new(),
    };

    format!(
        r#"你是法律文书信息抽取助手。从中国诉讼文书中抽取**所有能抽到的**字段,返回纯 JSON。
{}
========================================
JSON Schema(以下所有字段都返回,抽不到就 null 或 []):
========================================

【案件基本】
- case_no: 案号(必须 "(yyyy)xxxx号" 格式,不符合返 null)
- case_type: "民事" / "刑事" / "行政" / "仲裁" / "执行" / "非诉"
- court: 承办机关全称 —— 法院案件填"XX人民法院";仲裁案件填仲裁机构全称(如"XX劳动人事争议仲裁委员会"/"XX仲裁委员会"),**不要把仲裁案件的承办机关写成法院**
- cause: 案由(如 "民间借贷纠纷"、"股权转让纠纷"、"买卖合同纠纷")
- case_stage: "立案" / "一审" / "二审" / "再审" / "执行" / "已结案"(根据文书类型推断)
- case_status: "进行中" / "已结案" / "已调解" / "已撤诉"
- filed_at: 立案/起诉日期 YYYY-MM-DD
- expected_close_at: 预计结案日期 YYYY-MM-DD(若文书提到审限届满日)
- case_note: 案件特别提示(如"涉及金额较大,需重点跟进",从文书摘要抽出 1 句话)

【当事人 ⚠️ 必须按"原诉视角"】
- plaintiffs: **原诉原告/申请人姓名数组**(自然人 + 公司全名)
- defendants: **原诉被告/被申请人姓名数组**
- third_parties: 第三人姓名数组
- ⚠️ **反诉文档警告**:如果当前文档是反诉(标题含"反诉"二字,或反诉状/反诉受理费等),
   文中"原告"指反诉原告(其实是原诉的被告)、"被告"指反诉被告(其实是原诉的原告)。
   **请仍以原诉视角填**:把原诉的原告填 plaintiffs,原诉的被告填 defendants,不要被反诉文档绕进去。
- party_contacts: 当事人联系方式,格式
  [{{ "party": "委托人/被告/林明远", "name": "联系人姓名", "role": "本人/代理人/法务负责人/法定代表人/家属",
       "phone": "电话或null", "email": "邮箱或null", "is_our_side": true/false/null }}]
  ⚠️ **只关心我方当事人**(委托方/我方代理的当事人)— is_our_side=true。
  对方当事人即使有电话也填 is_our_side=false(前端只显示 true 的)。
  判断依据:
    · 委托合同/代理合同里的"委托方/甲方"= 我方
    · 起诉状抬头的"原告"通常 = 我方(我们替原告起诉)
    · 应诉通知/答辩状抬头的"答辩人/被告"= 我方(我们替被告答辩)
  如果一份文档判断不出我方,is_our_side 留 null。

【金额 / 收费 ⚠️ 优先级】
- claim_amount: 诉讼请求总金额(纯数字,人民币元,不要千分位逗号、不要"万")
- fees: 收费记录数组,格式
  [{{ "item": "...", "amount": 数字, "charged_at": "YYYY-MM-DD或null",
       "receipt_no": "凭证号或null", "note": "备注或null" }}]
  ⚠️ **优先级顺序**(重要程度递减):
    1) 律师代理费 / 律师费(从委托合同抽,这是最重要的!委托合同里通常 "代理费 X 元"、
       "服务费 X 元"、分期支付的写 "首期 X 元,二期 X 元",分两条 fees 记录)
    2) 保函费(申请财产保全时,保险公司/担保公司收的保函费)
    3) 案件受理费(法院判决书末尾"案件受理费 X 元由 XX 承担"、缴费凭证)
    4) 财产保全费 / 保全申请费(向法院交的保全费)
    5) 鉴定费 / 评估费 / 公证费 等
  每条 fees 必须有 item;amount/charged_at/receipt_no 抽不到就 null。

【法院人员】
- judges: 承办法官姓名数组(如 ["王某某"] / 合议庭 ["张三","李四","王五"])
- court_contacts: 法院人员联系方式,格式
  [{{ "name": "姓名", "role": "主办法官/审判员/审判长/法官助理/书记员", "phone": "电话或null" }}]
  ⚠️ 法院文书(判决/裁定/调解/笔录/传票/通知书)末尾**一定有**"审判员 XXX"和"书记员 XXX",必须抽,有电话也抽
  ⚠️ 同一个人不要因为既是"签发人"又是"审判员"就抽两条—**只抽审判员/书记员/法官助理这种实质角色**,
     "签发人/送达人/校对"这些次要角色丢掉

【时间线 / 保全 ⚠️ event_type 白名单】
- key_dates: 办案过程关键节点,格式
  [{{ "event_type": "<下面白名单之一>", "date": "YYYY-MM-DD或null", "note": "备注或null" }}]
  ⚠️ **event_type 只能是以下值之一**(其他类型一律不要抽,LPR/违约金计算/数字大写这些都不要):
    · 接案 — 委托合同签订日
    · 申请立案 — 起诉状提交日(向法院递交起诉材料)
    · 正式立案 — 法院立案受理通知书日期
    · 保全 — 财产保全申请或裁定日
    · 开庭 — 开庭日期(可多次,每次单独一条,note 优先写"开庭时间 + 法庭/地点 + 庭次备注",如"13:30 第6法庭"、"14:30-16:00 第6法庭 钱潮路369号801室";没有这些再写"庭前会议/第二次开庭"等)
    · 调解 — 调解书日期
    · 判决 — 判决书日期
    · 上诉 — 上诉状提交日
    · 二审开庭 — 二审开庭日期
    · 二审判决 — 二审判决书日期
    · 执行立案 — 执行案件立案日
    · 开发票 — 律师费/服务费发票开具日
  ⚠️ note 字段可以补充说明;对"开庭"节点,如果文书给了应到时间/庭审时间/法庭/地点,必须优先写进去,不要只写笼统"开庭"
- preservations: 财产保全记录,格式
  [{{ "target": "保全标的描述", "amount": 数字, "started_at": "YYYY-MM-DD或null",
       "duration_years": 2或3, "expires_at": "YYYY-MM-DD或null" }}]

========================================
重要规则:
========================================
1. **抽不到就返 null 或 []**,不要瞎编、不要返"无"、"待定"、"如(2024)..."这种占位
2. 法院文书末尾的"审判员 XXX""书记员 XXX""法官助理 XXX"**100% 必须抽到 court_contacts**
3. 金额一律纯数字(50000 不是 "5万"、不是 "50,000")
4. 日期一律 YYYY-MM-DD
5. 字段全部返回(包括空数组),不要省略
6. **反诉文档反过来填**:plaintiffs/defendants 按原诉视角,不要照搬反诉文档的"原告/被告"
7. **party_contacts 标 is_our_side**,只我方填 true

只返回纯 JSON,不要 markdown,不要解释。

文书内容:
---
{}
---

JSON:"#,
        context_line,
        text.trim()
    )
}
